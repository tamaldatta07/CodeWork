public class IfElseDemo {
    /*
     * Syntax of if statement
     * if(boolean expression True or False){
     * //body
     * }else{
     * // do this}
     */

    public static void main(String[] args) {
        int Salary = 25000;

        /*
         * if (Salary > 10000) {
         * Salary = Salary + 2000;
         * } else {
         * Salary = Salary + 1000;
         * }
         */

        // Multiple if-else
        /*
         * if (Salary > 10000) {
         * Salary += 2000;
         * } else if (Salary > 20000) {
         * Salary += 5000;
         * } else {
         * Salary += 4000;
         * }
         */
        System.out.println(Salary);
    }
}
